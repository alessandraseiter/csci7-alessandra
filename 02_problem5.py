#Enter a number and display whether it's a leap year

#Prompt user to enter a year. Verify that it is a valid year, then store it in a variable called user_year
user_entry = input('Please enter a year.')
assert( user_entry.isdigit() )
user_year = int(user_entry)

#Display whether user_year is a leap year
if user_year % 400 == 0:
    print(user_year,'is a leap year')
elif user_year % 4 == 0 and user_year % 100 != 0:
    print(user_year,'is a leap year')
else:
    print(user_year,'is not a leap year')


